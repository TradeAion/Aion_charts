/**
 * Pre-instantiate the wasm engine, optionally from an explicit URL. Only needed when the default
 * resolution (the `.wasm` shipped beside `dist/index.js`) doesn't apply — e.g. Vite's dev
 * pre-bundler (`import wasm_url from "@tradeaion/charts/dist/aion_wasm_bg.wasm?url"`). Call once,
 * before the first `create_chart`; later calls are no-ops.
 */
export function init_wasm(wasm_url?: string | URL): Promise<unknown> {
  return ensure_init(wasm_url);
}

/**
 * Create a chart inside `container`. Resolves once the rendering backend is ready and the first
 * frame is drawn. Mirrors the reference charting library's `createChart`, but async (backend acquisition).
 */
export async function create_chart(
  container: HTMLElement,
  options?: deep_partial<chart_options>,
): Promise<chart_api> {
  await ensure_init();

  // Four stacked, absolutely-positioned canvases fill the container: dedicated WebGPU and
  // Canvas2D panes below the transparent plugin overlay (Phase C-e canvas primitives), below the
  // transparent axis/input overlay. A canvas cannot change context type, so keeping the fallback
  // pane warm is what makes device-loss failover possible without recreating the chart or moving
  // state out of the headless engine. The plugin overlay is plain package-owned DOM (no wasm
  // involvement): it never takes input (gestures live on the top overlay), and the package
  // paints it after each engine frame.
  if (getComputedStyle(container).position === "static") {
    container.style.position = "relative";
  }
  const gpu_pane = document.createElement("canvas");
  const fallback_pane = document.createElement("canvas");
  const plugin_canvas = document.createElement("canvas");
  const overlay = document.createElement("canvas");
  for (const c of [gpu_pane, fallback_pane, plugin_canvas, overlay]) {
    c.style.position = "absolute";
    c.style.inset = "0";
    c.style.width = "100%";
    c.style.height = "100%";
    c.style.display = "block";
    // No `touch-action: none` (reference parity): the recognizer uses non-passive touch listeners and
    // conditionally preventDefaults, so a drag the chart doesn't own scrolls the page.
  }
  plugin_canvas.style.pointerEvents = "none";
  overlay.style.cursor = "crosshair";
  container.appendChild(gpu_pane);
  container.appendChild(fallback_pane);
  container.appendChild(plugin_canvas);
  container.appendChild(overlay);

  const rect = container.getBoundingClientRect();
  const css_w = Math.max(rect.width, 1);
  const css_h = Math.max(rect.height, 1);
  const dpr = window.devicePixelRatio || 1;
  // Bootstrap the bitmap size so either pane backend configures; auto-resize (if enabled) or a
  // manual resize() will correct it.
  for (const c of [gpu_pane, fallback_pane, plugin_canvas, overlay]) {
    c.width = Math.max(1, Math.round(css_w * dpr));
    c.height = Math.max(1, Math.round(css_h * dpr));
  }

  const simulate_adapter_failure = (
    options as (deep_partial<chart_options> & {
      __simulate_webgpu_adapter_failure?: boolean;
      __force_webgpu_fallback_adapter?: boolean;
    }) | undefined
  )?.__simulate_webgpu_adapter_failure === true;
  const force_fallback_adapter = (
    options as (deep_partial<chart_options> & { __force_webgpu_fallback_adapter?: boolean }) | undefined
  )?.__force_webgpu_fallback_adapter === true;
  const wasm = await wasm_create_chart(
    gpu_pane,
    fallback_pane,
    overlay,
    css_w,
    css_h,
    dpr,
    options?.backend === "canvas2d",
    simulate_adapter_failure,
    force_fallback_adapter,
  );
  // Default style settings first (theme.ts), explicit options second — the engine deep-merges
  // successive patches, so caller options always win over the theme palette. `theme`,
  // `handle_scroll`, `handle_scale`, `kinetic_scroll`, `tracking_mode`, and
  // `layout.panes.enableResize` are package-level keys and are not forwarded to the engine's
  // options store (gestures live entirely in TS).
  const { theme, handle_scroll, handle_scale, kinetic_scroll, tracking_mode, localization, ...rest } =
    (options ?? {}) as deep_partial<chart_options> & {
      tracking_mode?: tracking_mode_options;
    };
  let engine_options: Record<string, unknown> = rest;
  let panes_resize: boolean | undefined;
  const panes = (rest.layout as { panes?: { enableResize?: boolean } } | undefined)?.panes;
  if (panes?.enableResize !== undefined) {
    const { enableResize, ...panes_rest } = panes;
    engine_options = { ...rest, layout: { ...(rest.layout as object), panes: panes_rest } };
    panes_resize = enableResize;
  }
  wasm.apply_options(JSON.stringify(theme_options(theme ?? "light")));
  if (Object.keys(engine_options).length > 0) {
    wasm.apply_options(JSON.stringify(engine_options));
  }
  const auto_size = options?.autoSize === true;
  const chart = new chart_impl(wasm, container, gpu_pane, fallback_pane, plugin_canvas, overlay, auto_size);
  if (
    handle_scroll !== undefined || handle_scale !== undefined || kinetic_scroll !== undefined ||
    tracking_mode !== undefined
  ) {
    chart.apply_gesture_options(handle_scroll, handle_scale, kinetic_scroll, tracking_mode);
  }
  if (panes_resize !== undefined) {
    chart.apply_panes_resize(panes_resize);
  }
  if (localization !== undefined) {
    // deep_partial recurses into the callback signatures; the fields are already optional, so the
    // concrete localization_options shape is what apply_localization expects.
    chart.apply_localization(localization as localization_options);
  }
  chart.render();
  return chart;
}
